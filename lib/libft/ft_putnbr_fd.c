#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	int	mod;

	mod = 0;
	if (n == INT_MIN)
	{
		write(fd, "-2147483648", 11);
		return ;
	}
	if (n < 0)
	{
		n = n * -1;
		write(fd, "-", 1);
	}
	if (n > 0)
	{
		mod = n % 10 + '0';
		if (n / 10 != 0)
			ft_putnbr_fd(n / 10, fd);
	}
	if (n == 0)
		mod = 0 + '0';
	write(fd, &mod, 1);
}
