#include "libft.h"

void	*ft_memset(void *s, int c, size_t n)
{
	size_t	k;
	char	*str;

	str = s;
	k = 0;
	while (k != n)
	{
		*(str + k) = (char)c;
		k++;
	}
	return (str);
}
